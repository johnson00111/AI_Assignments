    graph = {}
    # example of graph structure:
    # graph = {'<s>': {'The': 0.5, 'A': 0.5}, 'The': {'cat': 1.0}, ...}

    sentences_add = "data//assign1_sentences.txt"

    def __init__(self):
        # Extract the directed weighted graph, and save to {head_word: {tail_word: probability}}
        with open(self.sentences_add, "r") as f:
            for line in f:
                words = line.strip().split()
                for i in range(len(words) - 1):
                    head = words[i]
                    tail = words[i + 1]
                    if head not in self.graph:
                        self.graph[head] = {}
                    if tail not in self.graph[head]:
                        self.graph[head][tail] = 1
                    else:
                        self.graph[head][tail] += 1

        # Convert counts to probabilities
        for head in self.graph:
            total_count = sum(self.graph[head].values())
            for tail in self.graph[head]:
                self.graph[head][tail] /= total_count

        print("*" * 100)
        print("Finished!")
        return

    def getProb(self, head_word, tail_word):
        # Return the probability of tail_word appearing after head_word
        if head_word in self.graph and tail_word in self.graph[head_word]:
            return self.graph[head_word][tail_word]
        else:
            return 0.0
